# Operadores aritmeticos
# Suma
n1=3
n2=2
r = n1 + n2
print("Suma:",r)
# Resta
n1=3
n2=2
r = n1 - n2
print("Resta:",r)
# Negación
n1=-3
n2=2
r = n1 - n2
print("Negación:",r)
# Multiplicación
n1=3
n2=2
r = n1 * n2
print("Multiplicación:",r)
# Potencia
n1=2
n2=3
r = n1 ** n2
print("Potencia de ",n1,":",r)
# División
n1=7
n2=2
r = n1 / n2
print("División:",r)
# División entera
n1=7
n2=2
r = n1 // n2
print("División entera:",r)
# Modulo
n1=9
n2=3
